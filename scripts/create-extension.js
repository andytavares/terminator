#!/usr/bin/env node
'use strict'

const fs = require('fs')
const path = require('path')

const NAME_REGEX = /^[a-z][a-z0-9-]{2,49}$/
const ID_REGEX = /^[a-z0-9]+(\.[a-z0-9-]+)+$/

// ── Validation ────────────────────────────────────────────────────────────────

function validateName(name) {
  if (!NAME_REGEX.test(name)) {
    return `Invalid name "${name}". Must be kebab-case, start with a letter, 3-50 chars (a-z, 0-9, -).`
  }
  return null
}

function validateId(id) {
  if (!ID_REGEX.test(id)) {
    return `Invalid ID "${id}". Must be a reverse-domain identifier like "com.example.my-ext".`
  }
  return null
}

// ── Template generators ───────────────────────────────────────────────────────

function generatePackageJson(name) {
  return JSON.stringify(
    {
      name: `@terminator/extension-${name}`,
      version: '0.1.0',
      private: true,
      description: `${name} extension for Terminator`,
      dependencies: {},
    },
    null,
    2
  )
}

function generateManifest(name, id) {
  const displayName = name
    .split('-')
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(' ')

  return JSON.stringify(
    {
      id,
      name: displayName,
      version: '0.1.0',
      description: `${displayName} extension for Terminator.`,
      main: 'src/index.js',
      minAppVersion: '0.1.0',
    },
    null,
    2
  )
}

